import java.util.*;

class StrWraper {
    String str;
    int level;
    
    public StrWraper(String str, int level) {
        this.str = str;
        this.level = level;
    }
}

class Solution {
    public boolean isNear(String str1, String str2) {
        if(str1.length() != str2.length()) {
            return false;
        }
        int count = 0;
        for(int i = 0; i < str1.length(); i++) {
            if(str1.charAt(i) != str2.charAt(i)) {
                count++;
            }
            if(count > 1) {
                return false;
            }
        }
        return true;
    }
    
    // Time Limit Exceeded
    public int ladderLength(String start, String end, HashSet<String> dict) {
        // Note: The Solution object is instantiated only once and is reused by each test case.
        // first round
        if(start.equals(end)) {
            return 0;
        }
        Iterator<String> iterator = dict.iterator(); 
        LinkedList<StrWraper> queue = new LinkedList<StrWraper>();
        while(iterator.hasNext()) {
            String str = iterator.next();
            if(isNear(start, str)) {
                queue.add(new StrWraper(str, 1));
                iterator.remove();
            }
        }
        while(!queue.isEmpty()) {
            StrWraper wraper = queue.poll();
            String current = wraper.str;
            if(isNear(current, end)) {
                return wraper.level + 2;
            }
            iterator = dict.iterator(); 
            while(iterator.hasNext()) {
                String str = iterator.next();
                if(isNear(current, str)) {
                    queue.add(new StrWraper(str, wraper.level + 1));
                    iterator.remove();
                }
            }
        }
        return 0;
    }

    public int ladderLength2(String start, String end, HashSet<String> dict) {
        // Note: The Solution object is instantiated only once and is reused by each test case.
        // first round
        if(start.equals(end)) {
            return 0;
        }
        LinkedList<StrWraper> queue = new LinkedList<StrWraper>();
        queue.add(new StrWraper(start, 0));
        while(!queue.isEmpty()) {
            StrWraper wraper = queue.poll();
            String current = wraper.str;
            for(int i = 0; i < current.length(); i++) {
                for(int j = 0; j < 26; j++) {
                    char ch = (char)('a' + j);
                    String change = current.substring(0, i) + ch + current.substring(i + 1);
                    if(change.equals(end)) {
                        return wraper.level + 2;
                    }
                    if(dict.contains(change)) {
                        queue.add(new StrWraper(change, wraper.level + 1));
                        dict.remove(change);
                    }
                }
            }            
        }
        return 0;
    }
}

